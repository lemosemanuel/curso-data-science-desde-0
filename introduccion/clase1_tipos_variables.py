# Tipos de variables

# Number (en Python, no es necesario declarar el tipo)
edad = 25
pi = 3.14159

# String
nombre = "Juan"
saludo = 'Hola, ¿cómo estás?'

# Boolean: (valores lógicos)
es_mayor_de_edad = True
esta_lloviendo = False

# None: (Una variable declarada pero no inicializada)
sin_definir = None

# Object: Contiene colecciones de propiedades
persona = {
    "nombre": "Juan",
    "edad": 25
}

# List: Lista ordenada de valores
numeros = [1, 2, 3, 4, 5]

